package poly.dto;

public class MusicalDTO {
	private String musical_num;
	private String musical_name;
	private String musical_date;
	private String musical_place;
	private String musical_story;
	private String musical_img;
	private String musical_rank;
	private String musical_score;
	private String musical_search;
	public String getMusical_num() {
		return musical_num;
	}
	public void setMusical_num(String musical_num) {
		this.musical_num = musical_num;
	}
	public String getMusical_name() {
		return musical_name;
	}
	public void setMusical_name(String musical_name) {
		this.musical_name = musical_name;
	}
	public String getMusical_date() {
		return musical_date;
	}
	public void setMusical_date(String musical_date) {
		this.musical_date = musical_date;
	}
	public String getMusical_place() {
		return musical_place;
	}
	public void setMusical_place(String musical_place) {
		this.musical_place = musical_place;
	}
	public String getMusical_story() {
		return musical_story;
	}
	public void setMusical_story(String musical_story) {
		this.musical_story = musical_story;
	}
	public String getMusical_img() {
		return musical_img;
	}
	public void setMusical_img(String musical_img) {
		this.musical_img = musical_img;
	}
	public String getMusical_rank() {
		return musical_rank;
	}
	public void setMusical_rank(String musical_rank) {
		this.musical_rank = musical_rank;
	}
	public String getMusical_score() {
		return musical_score;
	}
	public void setMusical_score(String musical_score) {
		this.musical_score = musical_score;
	}
	public String getMusical_search() {
		return musical_search;
	}
	public void setMusical_search(String musical_search) {
		this.musical_search = musical_search;
	}

	
	

}
